#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*st;
	int		x;

	x = 0;
	st = (char *)s;
	while (st[x])
		x++;
	while (x >= 0)
		if (st[x--] == c)
			return (st + x + 1);
	return (0);
}
